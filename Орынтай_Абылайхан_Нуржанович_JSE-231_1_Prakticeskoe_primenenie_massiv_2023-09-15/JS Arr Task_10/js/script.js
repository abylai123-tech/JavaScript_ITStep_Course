// Задание 10
let dynamicArray = [];
let size = +prompt("Введите размер массива");

// Заполняем массив значениями, которые вводит пользователь
for (let i = 0; i < size; i++) {
  dynamicArray[i] = +prompt("Введите значение элемента с индексом " + i);
}

console.log("Ваш динамический массив:", dynamicArray);

// Запрашиваем новое значение от пользователя
let newValue = +prompt("Введите новое значение для добавления");

// Запрашиваем индекс элемента, после которого нужно добавить новое значение
let insertIndex = +prompt("Введите индекс элемента, после которого нужно добавить новое значение");

// Проверяем, что индекс действительный
if (insertIndex >= 0 && insertIndex <= dynamicArray.length) {
  // Добавляем новое значение в массив после указанного индекса
  dynamicArray.splice(insertIndex + 1, 0, newValue);
  console.log("Массив после добавления нового значения:", dynamicArray);
} else {
  console.log("Введенный индекс недействителен. Пожалуйста, введите корректный индекс.");
}